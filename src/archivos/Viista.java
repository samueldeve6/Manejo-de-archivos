    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNombre.getText();
        String carpeta = System.getProperty("user.dir");
        String direccion = carpeta + "/" + nombre + ".txt";
        FileWriter ubicacion = null;
        try{
            ubicacion = new FileWriter(direccion);
        }catch(IOException ea) {
          Logger.getLogger(Archivo.class.getName()).log(Level.SEVERE,null,ea);
        }
        try {
            BufferedWriter escritor = new BufferedWriter(ubicacion);
            escritor.write(txtContenido.getText());
            escritor.close();
        } catch (Exception ex) {
            
        }
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(null);
        File archivo = fc.getSelectedFile();
        try{
        FileReader fr = new FileReader (archivo);
        BufferedReader b = new BufferedReader(fr);
        String texto="";
        String linea="";
        while(((linea=b.readLine())!=null) ){
             texto+=linea+"\n";
           }
        txtContenidoDos.setText(texto);
            JOptionPane.showMessageDialog(null,"Archivo leido exitosamente");
        }catch(Exception e){
            
        }
        
    }//GEN-LAST:event_btnSeleccionarActionPerformed

